package main

import "fmt"

func reversedString(s string) string {
	// –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Å—Ç—Ä–æ–∫—É –≤ —Å–ª–∞–π—Å —Ä—É–Ω, —Ç–∞–∫ –∫–∞–∫ —Å–∏–º–≤–æ–ª—ã Unicode –º–æ–≥—É—Ç –±—ã—Ç—å –±–æ–ª–µ–µ –æ–¥–Ω–æ–≥–æ –±–∞–π—Ç–∞.
	runes := []rune(s)
	// –ò—Ç–µ—Ä–∏—Ä—É–µ–º—Å—è –ø–æ –ø–æ–ª–æ–≤–∏–Ω–µ —Å–ª–∞–π—Å–∞, –º–µ–Ω—è—è –º–µ—Å—Ç–∞–º–∏ —Å–∏–º–≤–æ–ª—ã —Å –Ω–∞—á–∞–ª–∞ –∏ –∫–æ–Ω—Ü–∞.
	for i, j := 0, len(runes)-1; i < j; i, j = i+1, j-1 {
		runes[i], runes[j] = runes[j], runes[i]
	}
	// –í–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–æ–≤—É—é —Å—Ç—Ä–æ–∫—É, —Å–æ–∑–¥–∞–Ω–Ω—É—é –∏–∑ –∏–∑–º–µ–Ω–µ–Ω–Ω–æ–≥–æ —Å–ª–∞–π—Å–∞ —Ä—É–Ω.
	return string(runes)
}
func main() {
	input := "–≥–ª–∞–≤—Ä—ã–±–∞"
	reversed := reversedString(input)
	fmt.Println("–û—Ä–∏–≥–∏–Ω–∞–ª—å–Ω–∞—è —Å—Ç—Ä–æ–∫–∞:", input)
	fmt.Println("–ü–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç–∞—è —Å—Ç—Ä–æ–∫–∞:", reversed)

	// –°—Ç—Ä–æ–∫–∞ —Å Unicode —Å–∏–º–≤–æ–ª–∞–º–∏. –ù–∞–ø—Ä–∏–º–µ—Ä, —ç–º–æ–¥–∑–∏ –∑–∞–Ω–∏–º–∞—é—Ç –±–æ–ª–µ–µ –æ–¥–Ω–æ–≥–æ –±–∞–π—Ç–∞.
	str := "–ü—Ä–∏–≤–µ—Ç, –º–∏—Ä! üëã"

	// –ü–æ–ø—ã—Ç–∫–∞ –æ–±–æ–π—Ç–∏ —Å—Ç—Ä–æ–∫—É –ø–æ –±–∞–π—Ç–∞–º –∏ –≤–∑—è—Ç—å –ø–µ—Ä–≤—ã–µ 13 –±–∞–π—Ç–æ–≤.
	// –≠—Ç–æ –º–æ–∂–µ—Ç "—Ä–∞–∑—Ä–µ–∑–∞—Ç—å" —Å–∏–º–≤–æ–ª Unicode –ø–æ–ø–æ–ª–∞–º.
	first13Bytes := str[:15]

	fmt.Println("–û—Ä–∏–≥–∏–Ω–∞–ª—å–Ω–∞—è —Å—Ç—Ä–æ–∫–∞:", str)
	fmt.Println("–ü–µ—Ä–≤—ã–µ 13 –±–∞–π—Ç–æ–≤:", first13Bytes)
}
